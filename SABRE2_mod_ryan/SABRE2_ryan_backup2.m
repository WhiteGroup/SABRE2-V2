% function property_define_model_menu_Callback(hObject,eventdata)  
%    clc; pan off; zoom off; rotate3d off; set([vzm,vrm,vpm], 'Checked', 'off');
   if strcmp(get(gcbo,'type'),'uimenu') 
      OBJ=get(gcbo,'Tag');
   end 
   % Initially Auto Genetate BNodevalue for No Bracing Cases
   if ~isempty(Massemble)
      if isempty(BNodevalue)
         mem = length(Massemble(:,1));
         for i = 1:mem
            BNodevalue(i,1,1)=i; BNodevalue(i,1,2)=0; % 0 No bracing
         end
      elseif ~isequal(length(Massemble(:,1)),length(BNodevalue(:,1,1)))
         mem = length(Massemble(:,1));
         bn = length(BNodevalue(:,1,1));
         for i = (bn+1):mem
            BNodevalue(i,1,1)=i;
            BNodevalue(i,1,2)=0; % 0 No bracing
         end
      end 
   end  
   set(pt_title_name,'Visible','off');set(ri_infor_text,'Visible','off') 
   SABRE2Model(JNodevalue,Massemble,JNodevalue_i,JNodevalue_j, ...
      Rval,BNodevalue,vrum_az_slider,vrum_el_slider,vrum_az_edit,vrum_el_edit,axesm,vstm); 
